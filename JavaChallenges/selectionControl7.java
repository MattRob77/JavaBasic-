class Tester {
	public static void main(String[] args) {
		int day = 31;
		int month = 12;
		int year = 15;

		if(day == 31 && month == 12) {
		    System.out.println("1-1" + "-" + ++year);
		}
		else {
		    System.out.println("" + ++day + "-" + month + "-" + year);
		}
	}
}
